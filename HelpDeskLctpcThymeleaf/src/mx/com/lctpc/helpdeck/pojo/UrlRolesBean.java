package mx.com.lctpc.helpdeck.pojo;

public class UrlRolesBean {
	private String g_url;
	private String g_role;
	/**
	 * @return the url
	 */
	public String getUrl() {
		return this.g_url;
	}
	/**
	 * @param p_url the url to set
	 */
	public void setUrl( String p_url ) {
		this.g_url = p_url;
	}
	/**
	 * @return the role
	 */
	public String getRole() {
		return this.g_role;
	}
	/**
	 * @param p_role the role to set
	 */
	public void setRole( String p_role ) {
		this.g_role = p_role;
	}
	
	
	
	
}
